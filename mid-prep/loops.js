// JavaScript Loops

// 1. For Loop
console.log("\n1. For Loop");
for (let i = 0; i < 5; i++) {
  console.log(`Iteration ${i}`);
}

// 2. While Loop
console.log("\n2. While Loop");
let count = 0;
while (count < 5) {
  console.log(`Count is ${count}`);
  count++;
}

// 3. Do-While Loop
console.log("\n3. Do-While Loop");
let x = 0;
do {
  console.log(`x is ${x}`);
  x++;
} while (x < 5);
